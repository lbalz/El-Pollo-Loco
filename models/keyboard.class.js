/**
 * Class representing the keyboard input state for game controls
 */
class Keyboard {
    /** @type {boolean} Whether the left movement key is pressed */
    LEFT = false;

    /** @type {boolean} Whether the right movement key is pressed */
    RIGHT = false;

    /** @type {boolean} Whether the up/jump key is pressed */
    UP = false;

    /** @type {boolean} Whether the down key is pressed */
    DOWN = false;

    /** @type {boolean} Whether the space bar is pressed */
    SPACE = false;

    /** @type {boolean} Whether the throw action key is pressed */
    THROW = false;
}